function traducir (palabra) {
    switch (palabra){
        case "perro":
            console.log("dog");
            break;
        case "casa":
            console.log("house");
            break;
        case "mesa":
            console.log("table");
            break;
        case "gato":
            console.log("cat")
            break;
        default:
            console.log(" ");
    }
    
}
traducir("perro")